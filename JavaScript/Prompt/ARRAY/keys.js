/*

    <--> Array.prototype.keys()
                The keys() method returns a new Array Iterator object that contains the keys for each index in 
                the array.

                Syntax :=
                        keys()

                Example :=
                        let array1 = ['a', 'b', 'c'];
                        let iterator = array1.keys();
                        for (const key of iterator) {
                        console.log(key);                       // 0
                        }                                       // 1
                                                                // 2


*/