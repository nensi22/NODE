/*

    <--> Array.prototype.values()
                The values() method returns a new array iterator object that iterates the value of each 
                index in the array.

                Syntax :=
                        values()

                Example :=
                        let array = ['a', 'b', 'c'];
                        let iterator = array.values();
                        for (let value of iterator) {
                        console.log(value);                     // a
                        }                                       // b
                                                                // c
                

*/