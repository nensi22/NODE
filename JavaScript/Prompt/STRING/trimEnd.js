/*

    <--> String.prototype.trimEnd()
            The trimEnd() method removes whitespace from the end of a string and returns a new string, without modifying the original 
            string. trimRight() is an alias of this method.

            Syntax :=
                    trimEnd()
                    trimRight()

            Example :=
                    let str = '   Hello world!   ';
                    console.log(str);                          //     Hello world!
                    console.log(str.trimEnd());                //     Hello world!

*/ 