/*

    <--> String.prototype.toLocaleUpperCase()
            The toLocaleUpperCase() method returns the calling string value converted to upper case, according to any locale-specific
            case mappings.

            Syntax :=
                    toLocaleUpperCase()
                    toLocaleUpperCase(locales)
            
            Example :=
                    let city = 'istanbul';
                    console.log(city.toLocaleUpperCase('en-US'));            // ISTANBUL
                    console.log(city.toLocaleUpperCase('TR'));              //  ISTANBUL


*/