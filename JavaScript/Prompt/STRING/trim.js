/*

    <--> String.prototype.trim()
            The trim() method removes whitespace from both ends of a string and returns a new string, without modifying the original 
            string.

            Syntax :=
                        trim()

            Example :=
                    let str = '   Hello world!   ';
                    console.log(str);                                       //       Hello world!
                    console.log(str.trim());                                // Hello world!

*/  