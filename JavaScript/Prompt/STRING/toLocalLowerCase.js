/*

    <--> String.prototype.toLocaleLowerCase()
            The toLocaleLowerCase() method returns the calling string value converted to lower case, according to any locale-specific 
            case mappings.

            Syntax :=
                    toLocaleLowerCase()
                    toLocaleLowerCase(locales)

            Example :=
                    let dotted = 'Nensi';
                    console.log(`EN-US: ${dotted.toLocaleLowerCase('en-US')}`);             // EN-US: nensi
                    console.log(`TR: ${dotted.toLocaleLowerCase('tr')}`);                   // TR: nensi


*/